package tdc.edu.com.example.soccer_social_network.Data_Models;

public class ListSoccerDetail {
    String name;
    String type;
    byte[] image;

    public ListSoccerDetail(String name, String type, byte[] image) {
        this.name = name;
        this.type = type;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public byte[] getImage() {
        return image;
    }

    public void setImage(byte[] image) {
        this.image = image;
    }
}
